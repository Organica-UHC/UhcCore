package com.gmail.val59000mc.kit.upgrade;

public class KitUpgrade {

    private final int cost;
    private final int level;

    public KitUpgrade(int cost, int level) {
        this.cost = cost;
        this.level = level;
    }

    public int getCost() {
        return cost;
    }

    public int getLevel() {
        return level;
    }

}
